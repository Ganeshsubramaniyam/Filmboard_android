public class Music_Album_Item {

    String albumname;
    String albumid;
    String albumcomposername;
    int albumtrackscount;

    public String getAlbumname() {
        return albumname;
    }

    public void setAlbumname(String albumname) {
        this.albumname = albumname;
    }

    public String getAlbumid() {
        return albumid;
    }

    public void setAlbumid(String albumid) {
        this.albumid = albumid;
    }

    public String getAlbumcomposername() {
        return albumcomposername;
    }

    public void setAlbumcomposername(String albumcomposername) {
        this.albumcomposername = albumcomposername;
    }

    public int getAlbumtrackscount() {
        return albumtrackscount;
    }

    public void setAlbumtrackscount(int albumtrackscount) {
        this.albumtrackscount = albumtrackscount;
    }
}
