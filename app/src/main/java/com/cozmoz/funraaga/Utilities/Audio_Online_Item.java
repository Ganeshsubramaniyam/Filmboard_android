public class Audio_Online_Item {
    String albumid;
    String albumname;
    String albumposter;
    String albumviews;
    String albumtype;

    public String getAlbumid() {
        return albumid;
    }

    public void setAlbumid(String albumid) {
        this.albumid = albumid;
    }

    public String getAlbumname() {
        return albumname;
    }

    public void setAlbumname(String albumname) {
        this.albumname = albumname;
    }

    public String getAlbumposter() {
        return albumposter;
    }

    public void setAlbumposter(String albumposter) {
        this.albumposter = albumposter;
    }

    public String getAlbumviews() {
        return albumviews;
    }

    public void setAlbumviews(String albumviews) {
        this.albumviews = albumviews;
    }

    public String getAlbumtype() {
        return albumtype;
    }

    public void setAlbumtype(String albumtype) {
        this.albumtype = albumtype;
    }
}
